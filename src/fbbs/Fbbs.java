package fbbs;

public class Fbbs {
	
	private int fbbsID;
	private String fbbsTitle;
	private String userID;
	private String fbbsDate;
	private String fbbsContent;
	private int fbbsAvailable;
	
	public int getFbbsID() {
		return fbbsID;
	}
	public void setFbbsID(int fbbsID) {
		this.fbbsID = fbbsID;
	}
	public String getFbbsTitle() {
		return fbbsTitle;
	}
	public void setFbbsTitle(String fbbsTitle) {
		this.fbbsTitle = fbbsTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getFbbsDate() {
		return fbbsDate;
	}
	public void setFbbsDate(String fbbsDate) {
		this.fbbsDate = fbbsDate;
	}
	public String getFbbsContent() {
		return fbbsContent;
	}
	public void setFbbsContent(String fbbsContent) {
		this.fbbsContent = fbbsContent;
	}
	public int getFbbsAvailable() {
		return fbbsAvailable;
	}
	public void setFbbsAvailable(int fbbsAvailable) {
		this.fbbsAvailable = fbbsAvailable;
	}
		
	
}
